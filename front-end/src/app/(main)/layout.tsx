"use client";

import {
  CircleUserRound,
  ChevronDown,
} from "lucide-react";
import Link from "next/link";
import { usePathname } from "next/navigation";

function NavPill({ href, label, active }: { href: string; label: string; active: boolean }) {
  return (
    <Link
      href={href}
      className={
        active
          ? "nav-pill-active flex items-center justify-center rounded-[20px] px-5 py-2"
          : "flex items-center justify-center rounded-[20px] border border-[var(--border-nav-inactive)] bg-transparent px-5 py-2 transition-colors hover:bg-[var(--lavender-bg)]"
      }
    >
      <span
        className={`text-[14px] font-medium tracking-[-0.1px] ${
          active ? "text-[var(--purple-primary)]" : "text-[var(--text-nav)]"
        }`}
      >
        {label}
      </span>
    </Link>
  );
}

export default function MainLayout({ children }: { children: React.ReactNode }) {
  const pathname = usePathname();

  return (
    <div className="flex h-full w-full flex-col bg-transparent font-poppins">
      <nav className="glass-nav flex h-16 shrink-0 items-center justify-between px-8">
        <div className="flex items-center gap-8">
          <Link href="/patients" className="gradient-logo text-[24px] font-medium tracking-[-0.1px]">
            diagnostic
          </Link>
          <div className="flex gap-2">
            <NavPill href="/patients" label="Patients" active={pathname === "/patients"} />
            <NavPill href="/schedule" label="Schedule" active={pathname === "/schedule"} />
          </div>
        </div>
        <div className="flex items-center gap-3">
          <CircleUserRound className="h-7 w-7 text-[var(--purple-primary)]" strokeWidth={1.5} />
          <span className="text-[14px] font-medium tracking-[-0.1px] text-[var(--text-primary)]">
            Dr. Patel
          </span>
          <ChevronDown className="h-4 w-4 text-[var(--text-nav)]" />
        </div>
      </nav>

      <div className="flex min-h-0 flex-1 flex-col">{children}</div>
    </div>
  );
}
